package com.example.design;

public class Users {
    String ForeName;
    String SurName;

    public Users(String foreName, String surName) {
        this.ForeName = foreName;
        this.SurName = surName;

    }

    public String getForeName() {
        return ForeName;
    }

    public String getSurName() {
        return SurName;
    }
}
